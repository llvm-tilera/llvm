#ifndef Tile64_SUBTARGET_H
#define Tile64_SUBTARGET_H

#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "Tile64GenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class Tile64Subtarget : public Tile64GenSubtargetInfo {

  std::string CPUString;
  InstrItineraryData InstrItins;

	bool IsVLIWEnabled;

public:
  Tile64Subtarget(StringRef TT, StringRef CPU, StringRef FS);

  const InstrItineraryData &getInstItineraryData() const { return InstrItins; }

	bool isVLIWEnabled() const { return IsVLIWEnabled; }

  /// ParseSubtargetFeatures - Parses features string setting specified 
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);
  
};

} // end namespace llvm

#endif

